def generate_dataset():
    """生成完整数据集"""
    data = []
    
    # 添加所有数据
    all_data = (
        THREAT_INTEL +
        VULNERABILITY +
        COMPLIANCE +
        OPERATIONS
    )
    
    for question, answer in all_data:
        item = {
            "messages": [
                {"role": "user", "content": question},
                {"role": "assistant", "content": answer}
            ]
        }
        data.append(item)
    
    # 数据增强：生成变体
    augmented = []
    for item in data:
        augmented.append(item)
        
        # 添加场景变体
        q = item["messages"][0]["content"]
        a = item["messages"][1]["content"]
        
        # 企业场景变体
        if "企业" not in q and "公司" not in q:
            aug_q = f"企业{q}"
            aug_a = f"针对企业场景：{a}"
            augmented.append({
                "messages": [
                    {"role": "user", "content": aug_q},
                    {"role": "assistant", "content": aug_a}
                ]
            })
    
    return augmented

def main():
    print("=" * 60)
    print("Cyber Model 训练数据生成")
    print("=" * 60)
    
    data = generate_dataset()
    
    # 打乱顺序
    random.seed(42)
    random.shuffle(data)
    
    # 保存
    OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    with open(OUTPUT_PATH, "w", encoding="utf-8") as f:
        for item in data:
            f.write(json.dumps(item, ensure_ascii=False) + "\n")
    
    print(f"✅ 生成 {len(data)} 条数据")
    print(f"📁 保存至: {OUTPUT_PATH}")
    
    # 统计
    categories = {
        "威胁情报": len(THREAT_INTEL) * 2,
        "漏洞分析": len(VULNERABILITY) * 2,
        "合规审查": len(COMPLIANCE) * 2,
        "安全运维": len(OPERATIONS) * 2,
    }
    print("\n数据分布:")
