def generate_dataset():
    """生成完整数据集"""
    data = []
    
    all_data = (
        SCENE_ANALYSIS +
        CONTENT_AUDIT +
        SAFETY_ANALYSIS +
        SMART_EDITING +
        TRAFFIC_SAFETY
    )
    
    for question, answer in all_data:
        item = {
            "messages": [
                {"role": "user", "content": [{"type": "text", "text": question}]},
                {"role": "assistant", "content": answer}
            ]
        }
        data.append(item)
    
    # 数据增强
    augmented = list(data)
    
    # 场景变体
    scene_prefixes = [
        "通过摄像头记录显示，",
        "视频回放显示，",
        "作为安全专家，请评价这段视频中",
        "请检查这段视频内容，",
    ]
    
    for item in data:
        q_text = item["messages"][0]["content"]
        if isinstance(q_text, list):
            q_text = q_text[0]["text"] if q_text else ""
        
        for prefix in scene_prefixes[:2]:
            aug_q = f"{prefix}{q_text}"
            augmented.append({
                "messages": [
                    {"role": "user", "content": [{"type": "text", "text": aug_q}]},
                    {"role": "assistant", "content": item["messages"][1]["content"]}
                ]
            })
    
    return augmented

def main():
    print("=" * 60)
    print("Video Model 训练数据扩充")
    print("=" * 60)
    
    data = generate_dataset()
    
    random.seed(42)
    random.shuffle(data)
    
    OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    with open(OUTPUT_PATH, "w", encoding="utf-8") as f:
        for item in data:
            f.write(json.dumps(item, ensure_ascii=False) + "\n")
    
    print(f"✅ 生成 {len(data)} 条数据")
    print(f"📁 保存至: {OUTPUT_PATH}")
    
    categories = {
        "场景分析": len(SCENE_ANALYSIS) * 3,
        "内容审核": len(CONTENT_AUDIT) * 3,
        "安全分析": len(SAFETY_ANALYSIS) * 3,
        "智能剪辑": len(SMART_EDITING) * 3,
        "交通安全": len(TRAFFIC_SAFETY) * 3,
    }
    print("\n数据分布:")
    for cat, count in categories.items():
        print(f"  {cat}: {count} 条")

if __name__ == "__main__":
    main()
