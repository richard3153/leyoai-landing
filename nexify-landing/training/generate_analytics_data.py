def generate_dataset():
    """生成完整数据集"""
    data = []
    
    all_data = (
        STATISTICAL_ANALYSIS +
        TREND_PREDICTION +
        INSIGHT_REPORTS +
        VISUALIZATION +
        ANOMALY_DETECTION +
        DECISION_SUPPORT
    )
    
    for question, answer in all_data:
        item = {
            "messages": [
                {"role": "user", "content": question},
                {"role": "assistant", "content": answer}
            ]
        }
        data.append(item)
    
    # 数据增强
    augmented = list(data)
    
    context_prefixes = [
        "作为数据分析师，",
        "在企业数据分析中，",
        "请帮我分析",
    ]
    
    for item in data:
        q = item["messages"][0]["content"]
        a = item["messages"][1]["content"]
        
        for prefix in context_prefixes[:1]:
            aug_q = f"{prefix}{q}"
            augmented.append({
                "messages": [
                    {"role": "user", "content": aug_q},
                    {"role": "assistant", "content": a}
                ]
            })
    
    return augmented

def main():
    print("=" * 60)
    print("Analytics Model 训练数据生成")
    print("=" * 60)
    
    data = generate_dataset()
    
    random.seed(42)
    random.shuffle(data)
    
    OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    with open(OUTPUT_PATH, "w", encoding="utf-8") as f:
        for item in data:
            f.write(json.dumps(item, ensure_ascii=False) + "\n")
    
    print(f"✅ 生成 {len(data)} 条数据")
    print(f"📁 保存至: {OUTPUT_PATH}")
    
    categories = {
        "统计分析": len(STATISTICAL_ANALYSIS) * 2,
        "趋势预测": len(TREND_PREDICTION) * 2,
        "洞察报告": len(INSIGHT_REPORTS) * 2,
        "可视化": len(VISUALIZATION) * 2,
        "异常检测": len(ANOMALY_DETECTION) * 2,
        "决策支持": len(DECISION_SUPPORT) * 2,
    }
    print("\n数据分布:")
    for cat, count in categories.items():
        print(f"  {cat}: {count} 条")

if __name__ == "__main__":
    main()
